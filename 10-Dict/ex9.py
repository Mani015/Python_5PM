# Update():Updates the dictionary with the elements from another dictionary
fruits={'apple':10,'banana':20,'mango':30,'kiwi':40,'graphes':50,'dragon':80,'lemon':5}
print(fruits)
# {'apple': 10, 'banana': 20, 'mango': 30, 'kiwi': 40, 'graphes': 50, 'dragon': 80, 'lemon': 5}


# Syn:dict.update({key:value,key:value})

fruits.update({'pappaya':100,'orange':30,'crustedapple':30})
print(fruits)
# {'apple': 10, 'banana': 20, 'mango': 30, 'kiwi': 40, 'graphes': 50, 'dragon': 80, 'lemon': 5, 'pappaya': 100, 'orange': 30, 'crustedapple': 30}


