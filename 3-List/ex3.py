# What is the difference between Homogeneous/Heterogeneous data structure?
# Homogeneous Data Structure – Data elements will be of same data type (ex: Array).
# Heterogeneous Data Structure – Data elements may not be of same data type (ex: List, Tuples, Sets etc…).


# Homogeneous:Data elements will be of same data type
x=[1,2,3,4,5,6,7]
# print(x)
# [1, 2, 3, 4, 5, 6, 7]

y=['a','b','c','d','e']
# print(y)
# # ['a', 'b', 'c', 'd', 'e']
# ex:Array
# -------------------------------------------><------------------------------------------------

# Heterogeneous Data Structure – Data elements may not be of same data type (ex: List, Tuples, Sets etc…).
x1=[1,12.0, 'python',True, False,6+3j]
print(x1)
# [1, 12.0, 'python', True, False, (6+3j)]
