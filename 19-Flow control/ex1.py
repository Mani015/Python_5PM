# Nested if:
# You can have if statements inside if statements, this is called nested if statements.

# What is the nested IF syntax?
# A nested if statement is an if-else statement with another if statement as the if body or the else body.
# Here's an example: if ( num > 0 ) // Outer if if ( num < 10 ) // Inner if System. out.
# Syntax:

# if condition:
#     block of body
#     if condition:
#         block of body
#     else:
#         block of body
# else:
#     block of body


