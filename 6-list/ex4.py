# reverse():reverse()	Reverses the order of the list
bikes=['pulsar150','royalnfield','herohonda','r15','rx100','dominar','tvsxl','apache','platina']
print(bikes)
# ['pulsar150', 'royalnfield', 'herohonda', 'r15', 'rx100', 'dominar', 'tvsxl', 'apache', 'platina']

bikes.reverse()
print(bikes)
# ['platina', 'apache', 'tvsxl', 'dominar', 'rx100', 'r15', 'herohonda', 'royalnfield', 'pulsar150']
